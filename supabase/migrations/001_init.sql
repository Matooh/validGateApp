create policy "profiles can view themselves"
  on public.profiles for select
  using (auth.uid() = id);

create policy "profiles can update themselves"
  on public.profiles for update
  using (auth.uid() = id);

create policy "authenticated users can see institutions"
  on public.institutions for select
  to authenticated
  using (true);

create policy "authenticated users can see courses in their institution"
  on public.courses for select
  to authenticated
  using (institution_id = public.current_user_institution_id());

create policy "guardians can see own links"
  on public.guardian_students for select
  to authenticated
  using (guardian_profile_id = auth.uid());

create policy "guardians can create own links"
  on public.guardian_students for insert
  to authenticated
  with check (guardian_profile_id = auth.uid());

create policy "guardians can delete own links"
  on public.guardian_students for delete
  to authenticated
  using (guardian_profile_id = auth.uid());

create policy "institution staff can see all students"
  on public.students for select
  to authenticated
  using (
    institution_id = public.current_user_institution_id()
    and public.current_user_role() in ('ADMIN', 'PORTERIA', 'DOCENTE')
  );

create policy "guardians can see linked students"
  on public.students for select
  to authenticated
  using (
    exists (
      select 1
      from public.guardian_students gs
      where gs.student_id = students.id
        and gs.guardian_profile_id = auth.uid()
    )
  );

create policy "staff can update students in institution"
  on public.students for update
  to authenticated
  using (
    institution_id = public.current_user_institution_id()
    and public.current_user_role() in ('ADMIN', 'PORTERIA', 'DOCENTE')
  );

create policy "guardians can view schedule blocks from linked students"
  on public.schedule_blocks for select
  to authenticated
  using (
    exists (
      select 1
      from public.students s
      join public.guardian_students gs on gs.student_id = s.id
      where s.course_id = schedule_blocks.course_id
        and gs.guardian_profile_id = auth.uid()
    )
    or (
      public.current_user_role() in ('ADMIN', 'PORTERIA', 'DOCENTE')
      and exists (
        select 1
        from public.courses c
        where c.id = schedule_blocks.course_id
          and c.institution_id = public.current_user_institution_id()
      )
    )
  );

create policy "guardians can view linked attendance"
